#!/bin/bash
# oct-token-track: Daily token usage tracking with model breakdown
# Usage: oct-token-track [--save]

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
source "${SCRIPT_DIR}/../lib/common.sh"

check_deps jq && ensure_dirs

SAVE_REPORT=false
[ "${1:-}" = "--save" ] && SAVE_REPORT=true

LOG_FILE="$OCT_LOG_DIR/tokens-${DATE}.md"

echo "📈 Token Usage Report — $DATE"
echo "============================"
echo ""

# Get session data from OpenClaw if available
if command -v openclaw &>/dev/null; then
    log_info "Fetching OpenClaw session data..."
    
    SESSIONS_JSON=$(timeout 10 openclaw sessions --json 2>/dev/null || echo '{"sessions":[]}')
    
    # Model breakdown
    echo "🤖 By Model"
    echo "-----------"
    
    MODEL_DATA=$(echo "$SESSIONS_JSON" | jq '
        .sessions 
        | group_by(.model) 
        | map({
            model: .[0].model,
            provider: .[0].modelProvider,
            sessions: length,
            input: (map(.inputTokens // 0) | add),
            output: (map(.outputTokens // 0) | add)
        })
        | sort_by(.input + .output)
        | reverse
    ' 2>/dev/null || echo "[]")
    
    echo "$MODEL_DATA" | jq -r '.[] | 
        "\nModel:     \(.model)
Provider:  \(.provider)
Sessions:  \(.sessions)
Input:     \(.input) tokens
Output:    \(.output) tokens
Total:     \(.input + .output) tokens"
    '
    
    # Provider breakdown
    echo ""
    echo "🏢 By Provider"
    echo "--------------"
    
    echo "$SESSIONS_JSON" | jq '
        .sessions 
        | group_by(.modelProvider) 
        | map({
            provider: .[0].modelProvider,
            sessions: length,
            tokens: (map((.inputTokens // 0) + (.outputTokens // 0)) | add)
        })
        | sort_by(.tokens)
        | reverse
        | .[]
        | "\(.provider): \(.sessions) sessions, \(.tokens) tokens"
    '
    
    # Totals
    TOTAL_INPUT=$(echo "$SESSIONS_JSON" | jq '[.sessions[].inputTokens // 0] | add // 0')
    TOTAL_OUTPUT=$(echo "$SESSIONS_JSON" | jq '[.sessions[].outputTokens // 0] | add // 0')
    TOTAL=$((TOTAL_INPUT + TOTAL_OUTPUT))
    SESSION_COUNT=$(echo "$SESSIONS_JSON" | jq '.sessions | length')
    
    echo ""
    echo "📊 Daily Summary"
    echo "----------------"
    echo "Date:           $DATE"
    echo "Total Sessions: $SESSION_COUNT"
    echo "Input Tokens:   $TOTAL_INPUT"
    echo "Output Tokens:  $TOTAL_OUTPUT"
    echo "Total Tokens:   $TOTAL"
    
    # Save report
    if [ "$SAVE_REPORT" = true ]; then
        {
            echo "# Token Usage — $DATE"
            echo ""
            echo "**Generated:** $TIMESTAMP"
            echo ""
            echo "## Summary"
            echo "- Total Sessions: $SESSION_COUNT"
            echo "- Input Tokens: $TOTAL_INPUT"
            echo "- Output Tokens: $TOTAL_OUTPUT"
            echo "- **Total Tokens: $TOTAL**"
            echo ""
            echo "## By Model"
            echo '```json'
            echo "$MODEL_DATA"
            echo '```'
            echo ""
            echo "---"
            echo "*Generated by OpenClaw Tools v$OCT_VERSION*"
        } > "$LOG_FILE"
        log_success "Report saved: $LOG_FILE"
    fi
    
else
    log_warn "OpenClaw CLI not available"
    echo "Install OpenClaw to track token usage"
    exit 1
fi
